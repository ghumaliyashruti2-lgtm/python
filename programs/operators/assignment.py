# assignement operators example

print("--->> assignement operators <<----")

#add and assign operator
print("--->> add and assign <<----")
a = 10
print("before a is : " ,a)
a += a
print ("after add and assign operator(a+=a): a is",a )

#sub and assign operator
print("--->> sub and assign <<----")
a = 10
print("before a is : " ,a)
a -= a
print ("after (a-=a): a is",a )

#multiply and assign operator
print("--->> multiply and assign <<----")
a = 10
print("before a is : " ,a)
a *= a
print ("after (a*=a): a is",a )

#divide and assign operator
print("--->> divide and assign <<----")
a = 10
print("before a is : " ,a)
a /= a
print ("after (a/=a): a is",a )

#modulo and assign operator
print("--->> modulo and assign <<----")
a = 10
print("before a is : " ,a)
a %= a
print ("after (a%=a): a is",a )

#floor division and assign operator
print("--->> floor division and assign  <<----")
a = 10
print("before a is : " ,a)
a //= a
print ("after (a//=a): a is",a )

#exponentiation and assign operator
print("--->> exponentiation and assign <<----")
a = 10
print("before a is : " ,a)
a **= a
print ("after (a**=a): a is",a )
