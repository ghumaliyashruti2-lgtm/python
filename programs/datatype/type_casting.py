# type casting example

print("--->> type casting example in int <<----")

a = "10"
print("before convert in datatype: ", a ," is " ,type(a))

b = int(a)
print("after convert in int datatype: ", b ," is " ,type(b))


c = input("enter the input : ")
print("before convert in datatype: ", c ," is " ,type(c))

d = int(c)
print("after convert in int datatype: ", d ," is " ,type(d))


e = int(input("enter the input : "))
print("direct convert in int datatype: ", e ," is " ,type(e))

