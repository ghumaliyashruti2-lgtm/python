#identity operators

print("--->> identity operators <<----")
x = ["apple", "banana"]
y = ["apple", "cherry"]
z = x

print("x: ",x)
print("y: ",y)
print("z: ",z)

#is operator
print("--->> is operator <<----")
print("x is z ? : ",x is z,"(yes its same object)")
print("x is y ? : ",x is y ,"(no object is not same )")

#is not
print("--->> is not operator <<----")
print("x is not z ? :",x is not z,"(no its same object)")
print("x is not y ? :",x is not y,"{yes its not same object)")

